por_extenso = (numero) => {
    switch (Math.floor(numero)) {

        case 10: console.log('Dez')
            break
        case 9: console.log('Nove')
            break
        case 8: console.log('Oito')
            break
        case 7: console.log('Sete')
            break
        case 6: console.log('Seis')
            break
        case 5: console.log('Cinco')
            break
        case 4: console.log('Quatro')
            break
        case 3: console.log('Três')
            break
        case 2: console.log('Dois')
            break
        case 1: console.log('Um')
            break
        case 0: console.log('Zero')
            break
        default: console.log('Número fora do intervalo')

    }
}

por_extenso(10)
por_extenso(9)
por_extenso(8)
por_extenso(7)
por_extenso(6)
por_extenso(5)
por_extenso(4)
por_extenso(3)
por_extenso(2)
por_extenso(1)
por_extenso(0)
